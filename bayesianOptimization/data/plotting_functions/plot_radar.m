function p = plot_radar(lambda)

    % DESCRIPTION:                  creates radar graph of eigenvalues
    % INPUT: lambda [n x m]         matrix with n rows of m eigenvalues

    % Spider plot
    p = spider_plot_class(lambda,...
        'AxesLabels', {'$\psi$','$\phi$','$\theta$','$x$', '$y$', '$z$',...
        '$\omega_x$','$\omega_y$','$\omega_z$','$v_x$','$v_y$','$v_z$'},...
        'LegendLabels', {'DCMPC','cMPC'},...
        'AxesDisplay', 'data',...
        'AxesLimits', [zeros(1,12); 1.5*ones(1,12)],...
        'AxesInterval', 1,...
        'FillOption',{'on', 'on'},...
        'FillTransparency', [0.4, 0.4],...
        'AxesFontSize',12,...
        'AxesFont','Times',...
        'LabelFontSize',16,...
        'LabelFont','Times',...
        'AxesDataOffset', 0.25,...
        'AxesLabelsEdge','w',...
        'AxesOffset',1,...
        'AxesLabelsOffset',.3,...
        'AxesInterpreter','latex',...
        'AxesColor', [.9 .9 .9],...
        'Direction', 'counterclockwise');