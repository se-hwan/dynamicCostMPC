function p = plot_radar(lambda)

    % DESCRIPTION:                  creates radar graph of eigenvalues
    % INPUT: lambda [n x m]         matrix with n rows of m eigenvalues

    % Spider plot
    p = spider_plot_class(lambda,...
        'AxesLabels', {'$\lambda_1$','$\lambda_2$','$\lambda_3$','$\lambda_4$', ...
                       '$\lambda_4$','$\lambda_6$','$\lambda_7$','$\lambda_8$', ...
                       '$\lambda_9$','$\lambda_{10}$','$\lambda_{11}$','$\lambda_{12}$'},...
        'LegendLabels', {'v_x = 2.0','v_x = 2.5'},...
        'AxesDisplay', 'data',...
        'AxesLimits', [zeros(1,12); 100*ones(1,12)],...
        'AxesInterval', 10,...
        'AxesFontSize',8,...
        'AxesFont','Times',...
        'LabelFontSize',16,...
        'LabelFont','Times',...
        'AxesDataOffset', 0.15,...
        'AxesLabelsEdge','w',...
        'AxesOffset',1,...
        'AxesLabelsOffset',.3,...
        'AxesInterpreter','latex',...
        'AxesColor', [.9 .9 .9],...
        'Direction', 'counterclockwise');


%         'FillOption',{'on', 'on'},...
%         'FillTransparency', [0.4, 0.4],...